    @Override
    public void invalidateAll() {
        synchronized(this) {
                mDirtyFlags = 0x4L;
        }
        requestRebind();
    }

    @Override
    public boolean hasPendingBindings() {
        synchronized(this) {
            if (mDirtyFlags != 0) {
                return true;
            }
        }
        return false;
    }

    @Override
    public boolean setVariable(int variableId, @Nullable Object variable)  {
        boolean variableSet = true;
        if (BR.activity == variableId) {
            setActivity((com.wf.weatherforcastexample.MainActivityActivity) variable);
        }
        else if (BR.viewModel == variableId) {
            setViewModel((com.wf.weatherforcastexample.ui.home.viewmodel.HomeViewModel) variable);
        }
        else {
            variableSet = false;
        }
            return variableSet;
    }

    public void setActivity(@Nullable com.wf.weatherforcastexample.MainActivityActivity Activity) {
        this.mActivity = Activity;
    }
    public void setViewModel(@Nullable com.wf.weatherforcastexample.ui.home.viewmodel.HomeViewModel ViewModel) {
        this.mViewModel = ViewModel;
    }

